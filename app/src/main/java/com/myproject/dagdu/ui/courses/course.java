package com.myproject.dagdu.ui.courses;

public class course {
    String id;
    String imagepath;
    String name;
    String price;

    public course(String id, String imagepath, String name, String price) {
        this.id = id;
        this.imagepath = imagepath;
        this.name = name;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImagepath() {
        return imagepath;
    }

    public void setImagepath(String imagepath) {
        this.imagepath = imagepath;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
